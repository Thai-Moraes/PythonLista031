'''
Fazer um algoritmo que pergunte 3 números e apresente a média aritmética entre estes 3 números.
'''

print("\n[PROGRAMA DE MÉDIA ARITMÉTICA]\n")
num1 = int(input("Informe um número: "))
num2 = int(input("Informe outro número: "))
num3 = int(input("Informe outro outro número: "))

print(f'A sua média é {(num1 + num2 + num3) / 3}')